package br.com.supermarket.model;

public class CategoryItem {
    private long id;
    private String name;

    public CategoryItem(String name) {
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
